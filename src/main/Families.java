package main;

public class Families {

	public static String SelectFamily(String family) {
		if(family == "A"){
			return family;
		}
		else if(family == "B"){
			return family;
		}
		else if(family == "C"){
			return family;
		}
		else {
			return "Please select Family A, B, or C only.";
		}
	}
}
